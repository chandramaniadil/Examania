<h2>Graduate Aptitude Test in Engineering (GATE)</h2> &nbsp;&nbsp;&nbsp;&nbsp; is an all India examination that primarily tests the comprehensive understanding of the candidate in various undergraduate subjects in Engineering/Technology/Architecture and post-graduate level subjects in Science. The GATE score of a candidate reflects a relative performance level in a particular subject in the exam across several years. The score is used for admissions to post-graduate programs (e.g., M.E., M.Tech, direct Ph.D.) in Indian institutes of higher education with financial assistance provided by MHRD and other Government agencies. The score may also be used by Public and Private Sector Undertakings for employment screening purposes.<br />
<h2> About GATE</h2>
The Indian Institute of Science (IISc) and seven Indian Institutes of Technology (IITs at Bombay, Delhi, Guwahati, Kanpur, Kharagpur, Madras and Roorkee) jointly administer the conduct of GATE. The operations related to GATE in each of the 8 zones are managed by a zonal GATE Office at the IITs or IISc. The Organizing Institute (OI) is responsible for the end-to-end process and coordination amongst the administering Institutes.



